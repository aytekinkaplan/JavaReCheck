package Java002_Operators;

public class ArithMeticExample004 {
    public static void main(String[] args) {
        int x = 8;
        System.out.println(x);
        x+=3;
        System.out.println(x);
        x-=4;
        System.out.println(x);
        x*=3;
        System.out.println(x);
        x/=2;
        System.out.println(x);
        x%=3;
        System.out.println(x);
    }
}
